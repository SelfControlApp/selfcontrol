      			  - appcast.xml (contains the '<item>' info)
      			  - 1.1.html
      			  - rnotes.css
      			  - ProjectName 1.1.zip
    
    
    The following files are created for you if they do not already exist.
        appcast_xml_name:
        css_file_name:
        version_number.html

