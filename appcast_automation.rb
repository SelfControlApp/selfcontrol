    					</td>
    				</tr>
    				<tr>
    					<td valign=\"top\" width=\"150\"><img src=\"IMAGE_NAME.png\" alt=\"ALT_TITLE\"  width=\"150\" border=\"0\"></td>
    					<td valign=\"top\">
    						<p>DESCRIPTION</p>
    					</td>
    				</tr>
    			</table>
    			<br>
    	</body>

    </html>"    
  end
  
